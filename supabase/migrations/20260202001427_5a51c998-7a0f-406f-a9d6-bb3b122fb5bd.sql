-- Create storage bucket for reward product images
INSERT INTO storage.buckets (id, name, public)
VALUES ('reward-products', 'reward-products', true)
ON CONFLICT (id) DO NOTHING;

-- Allow anyone to view product images (public bucket)
CREATE POLICY "Product images are publicly accessible"
ON storage.objects FOR SELECT
USING (bucket_id = 'reward-products');

-- Allow authenticated users to upload product images (admin will handle this)
CREATE POLICY "Authenticated users can upload product images"
ON storage.objects FOR INSERT
WITH CHECK (bucket_id = 'reward-products' AND auth.uid() IS NOT NULL);

-- Allow authenticated users to update their uploads
CREATE POLICY "Authenticated users can update product images"
ON storage.objects FOR UPDATE
USING (bucket_id = 'reward-products' AND auth.uid() IS NOT NULL);

-- Allow authenticated users to delete product images
CREATE POLICY "Authenticated users can delete product images"
ON storage.objects FOR DELETE
USING (bucket_id = 'reward-products' AND auth.uid() IS NOT NULL);